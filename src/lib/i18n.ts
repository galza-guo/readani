export type MessageKey = keyof typeof EN_MESSAGES;

const LOCALE_MAP: Record<string, Record<string, string>> = {};

let currentLocale = "en";

export function setLocale(code: string) {
  if (LOCALE_MAP[code]) {
    currentLocale = code;
    return;
  }
  const base = code.split("-")[0];
  if (base && LOCALE_MAP[base]) {
    currentLocale = base;
    return;
  }
  currentLocale = "en";
}

export function getLocale() {
  return currentLocale;
}

export function t(key: MessageKey, variables?: Record<string, string>) {
  const localeMessages = LOCALE_MAP[currentLocale];
  let message: string | undefined =
    localeMessages?.[key] ?? EN_MESSAGES[key] ?? key;

  if (!variables) {
    return message;
  }

  for (const [name, value] of Object.entries(variables)) {
    message = message.split(`{${name}}`).join(value);
  }

  return message;
}

export function addLocale(code: string, messages: Record<string, string>) {
  LOCALE_MAP[code] = messages;
}

export function hasLocaleMessage(key: MessageKey, locale = currentLocale) {
  return Boolean(LOCALE_MAP[locale]?.[key]);
}

addLocale("en", EN_MESSAGES);
